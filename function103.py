#Python function arguments
def add(a,b):
    sum = a + b
    print("Sum =" , sum)
add(10,5) #50

def multiply(a,b):
    sum = a * b
    print("Sum =",sum)
multiply(100,5) #500

def division(a,b):
    sum = a / b
    print("Sum =",sum)
division(1000,500) #2.0

def delete(a,b,c=50):
    sum = a - b - c
    print("Sum =",sum)
delete(1000,100)#850